/** Tabs used to switch between different datasets within a chart card */
function ChartTabs({
  tabs,
  active,
  onChange,
}: {
  tabs: { id: string; label: string }[];
  active: string;
  onChange: (id: string) => void;
}) {
  return (
    <div className="mb-4 flex flex-wrap gap-1.5">
      {tabs.map((t) => (
        <button
          key={t.id}
          onClick={() => onChange(t.id)}
          className={`rounded-md border px-3 py-1.5 text-xs font-medium transition-colors duration-150 ${active === t.id
              ? "border-primary bg-primary text-primary-foreground shadow-sm"
              : "border-border bg-muted text-muted-foreground hover:bg-muted/80 hover:text-foreground"
            }`}
        >
          {t.label}
        </button>
      ))}
    </div>
  );
}

