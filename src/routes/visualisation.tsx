/**
 * Map a correlation value from [-1, 1] to a clean diverging scale
 * deep blue (−1) → white (0) → deep red (+1) (ColorBrewer RdBu endpoints).
 * Near-zero values are white, matching the legend.
 */
function corrColor(value: number): string {
  const t = Math.min(1, Math.abs(value));
  // Endpoints: white (247,247,247), red (178,24,43), blue (33,102,172)
  const [er, eg, eb] = value >= 0 ? [178, 24, 43] : [33, 102, 172];
  const r = Math.round(247 + (er - 247) * t);
  const g = Math.round(247 + (eg - 247) * t);
  const b = Math.round(247 + (eb - 247) * t);
  return `rgb(${r}, ${g}, ${b})`;
}

