// Choropleth fill: probability ~0–100% → light → deep blue (MACRON / Centre)
function deptFill(proba: number): string {
  const t = Math.min(1, Math.max(0, proba / 100));
  const lightness = Math.round(85 - t * 47);
  return `hsl(214, 70%, ${lightness}%)`;
}

